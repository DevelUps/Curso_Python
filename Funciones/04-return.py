def suma (a, b):
    """Esta funcion suma dos numeros"""
    resultado = a + b #aqui se hace la suma de los argumentos y se devuelve el resultado
    return resultado #aqui se devuelve el resultado de la suma
suma(1,2) #aqui se llama a la funcion y se le pasan los argumentos
c = suma(1,2) #aqui se llama a la funcion y se le pasan los argumentos
d = suma(c,2) #aqui se llama a la funcion y se le pasan los argumentos
print(d) #aqui se imprime el resultado de la suma