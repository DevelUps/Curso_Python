# Operadores l√≥gicos en Python

a = True
b = False
resultado_and = a and b  # False porque ambos no son verdaderos
print(f"Resultado de {a} and {b}: {resultado_and}")

# Ejemplo de operador or
c = True
d = False
resultado_or = c or d  # True porque al menos uno es verdadero
print(f"Resultado de {c} or {d}: {resultado_or}")

# Ejemplo de operador not
e = True
resultado_not = not e  # False porque not invierte el valor
print(f"Resultado de not {e}: {resultado_not}")